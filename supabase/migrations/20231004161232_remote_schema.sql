CREATE OR REPLACE FUNCTION "public"."handle_new_user"() RETURNS "trigger"
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$begin
  insert into public.users (auth_user_id, id, email)
  values (new.id, gen_random_uuid(), new.email);
  return new;
end;
$$;

